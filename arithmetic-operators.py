# Solution : 
x = 20
y = 6
# Addition 
print("Addition:", x + y)
# Subtraction 
print("Subtraction:", x - y)
# Multiplication
print("Multiplication:", x * y)
# Division
print("Division:", x / y)
# Floor Division
print("Floor Division:", x // y)
# Modulus
print("Modulus:", x % y)
# Exponent
print("Exponent:", x ** y)
